#ENTRADAS
#usuario informa dois numeros x e y
x = float(input("Informe o primeiro valor: "))
y = int(input("Informe o segundo valor: "))

#PROCESSAMENTO
#a partir de x e y, calulcar a soma
soma = x + y
subtracao = x - y
multiplicacao = x * y
divisao = x / y

#SAIDAS
#exibir a soma calculada
print(f"A soma dos valores é {soma}")
print(f"A subtração é {subtracao}")
print(f"A multiplicação é {multiplicacao}")
print(f"A divisão é {divisao}")